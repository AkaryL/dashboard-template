// UTILITY: Get all articles as a flat array
// ============================================================

export const getAllArticles = () => {
  return Object.values(newsData).flat();
};

// ============================================================
// UTILITY: Find article by slug
// ============================================================

export const getArticleBySlug = (slug) => {
  const allArticles = [...getAllArticles(), ...latestNews];
  return allArticles.find((article) => article.slug === slug) || null;
};

